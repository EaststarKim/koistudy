char c[30];
int ans=9;
void f(int p){
    if(p>=ans)return;
    int i,j,k;
    char s[30],t[30];
    for(i=0;i<24;i+=4){
        for(j=1;j<4;++j)if(c[i+j]!=c[i])break;
        if(j<4)break;
    }
    if(i==24){
        ans=p;
        return;
    }
    for(i=0;i<24;++i)t[i]=c[i];
    for(i=1;i<7;++i){
        for(k=1;k<3;++k){
            for(j=0;j<24;++j)s[j]=c[trans[i][j]];
            for(j=0;j<24;++j)c[j]=s[j];
            f(p+k);
        }
        for(j=0;j<24;++j)c[j]=t[j];
    }
}
int main(){
    scanf("%s",c);
    f(0);
    printf("%d",ans);
    return 0;
}
