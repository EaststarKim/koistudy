#include<stdio.h>
int d[205][205][4];//0=UU 1=RR 2=UR 3=RU
int main()
{
    int a,b,i,j,x,y;
    scanf("%d%d",&a,&b);
    d[1][2][0]=d[2][1][1]=1;
    for(i=4;i<=a+b;i++)
	{
        for(j=1;j<=i;j++)
		{
			x=j;
			y=i-j;
            d[x][y][0]=d[x][y-1][0]+d[x][y-1][3];
            d[x][y][1]=d[x-1][y][1]+d[x-1][y][2];
            d[x][y][2]=d[x-1][y][0];
            d[x][y][3]=d[x][y-1][1];
            if (d[x][y][0]>=100000)d[x][y][0]%=100000;
            if (d[x][y][1]>=100000) d[x][y][1]%=100000;
            if (d[x][y][2]>=100000) d[x][y][2]%=100000;
            if (d[x][y][3]>=100000) d[x][y][3]%=100000;
        }
    }
    printf("%d",(d[a][b][0]+d[a][b][1]+d[a][b][2]+d[a][b][3])%100000);
    return 0;
}